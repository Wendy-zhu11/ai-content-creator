/**
 * 构建完整的提示词
 */
export function buildPrompt(
  type: ContentType,
  input: string,
  options?: GenerateOptions
): string {
  const systemPrompt = contentPrompts[type]

  let userPrompt = input

  // 添加可选参数
  if (options) {
    const optionsText = []
    if (options.tone) {
      const toneMap = {
        professional: '专业正式',
        casual: '轻松随意',
        friendly: '友好亲切',
        formal: '正式严肃',
      }
      optionsText.push(`语气风格：${toneMap[options.tone]}`)
    }
    if (options.length) {
      const lengthMap = {
        short: '简短（200-500字）',
        medium: '中等（500-1000字）',
        long: '详细（1000-2000字）',
      }
      optionsText.push(`篇幅要求：${lengthMap[options.length]}`)
    }
    if (options.language) {
      const langMap = {
        zh: '中文',
        en: '英文',
      }
      optionsText.push(`语言：${langMap[options.language]}`)
    }
    if (optionsText.length > 0) {
      userPrompt = `${input}\n\n${optionsText.join('\n')}`
    }
  }

  return `${systemPrompt}\n\n用户需求：\n${userPrompt}`
}
